    public function changeRole($id)
    {
        $user = User::find($id);
        if ($user->role == 2) {
            $user->role = 3;
            $user->save();
        } else if ($user->role == 3) {
            $user->role = 2;
            $user->save();
        }

        return back()->with('success', 'Berhasil Ganti Role');
